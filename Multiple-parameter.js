function addNumber(a,b){
    var result=a+b;
    return result;
}
var num1 =10;
var num2=20;
var sum=addNumber(num2,num1);
console.log(sum);

function multi(number1,number2){
    var value=number1*number2;
    return value;
}
var num3=20;
var num4=5;
var value1=multi(num3,num4);
console.log(value1);